const uniqueNames = new Set(artifacts.map(artifact => artifact.name))
assert(uniqueNames.size === artifacts.length, 'artifact coverage matrix contains duplicate artifact names')

for (const artifact of artifacts) {
  assert(metadataGateScript.includes(`'${artifact.name}'`), `P43 metadata gate script must require ${artifact.name}`)
  assert(p43.includes(artifact.name), `P43 doc must include ${artifact.name}`)
  assert(p16.includes(artifact.name), `P16 doc must include ${artifact.name}`)
  assert(p107.includes(artifact.name), `P107 doc must include ${artifact.name}`)
  assert(workflow.includes(artifact.uploadStep), `Pages workflow must include upload step ${artifact.uploadStep}`)
  assert(workflow.includes(artifact.uploadPath), `Pages workflow must upload ${artifact.uploadPath}`)

  if (artifact.kind === 'download_content_gate') {
    assert(packageJson.scripts[artifact.verifier], `package.json must expose ${artifact.verifier}`)
    assert(String(packageJson.scripts.test).includes(`npm run ${artifact.verifier}`), `root test must include ${artifact.verifier}`)
    assert(workflow.includes(artifact.workflowStep), `Pages workflow must include ${artifact.workflowStep}`)
    assert(workflow.includes(`npm run ${artifact.verifier}`), `Pages workflow must run ${artifact.verifier}`)
    assert(p43.includes(artifact.verifier), `P43 doc must mention ${artifact.verifier}`)
    assert(p107.includes(artifact.contract), `P107 doc must mention ${artifact.contract}`)
  }

  if (artifact.kind === 'pre_upload_generator_gate') {
    assert(packageJson.scripts[artifact.producer], `package.json must expose ${artifact.producer}`)
    assert(packageJson.scripts[artifact.verifier], `package.json must expose ${artifact.verifier}`)
    assert(String(packageJson.scripts.test).includes(`npm run ${artifact.verifier}`), `root test must include ${artifact.verifier}`)
    assert(p107.includes(artifact.verifier), `P107 doc must mention ${artifact.verifier}`)
  }

  if (artifact.kind === 'visual_human_evidence') {
    assert(packageJson.scripts[artifact.producer], `package.json must expose ${artifact.producer}`)
    assert(workflow.includes('Run local live runtime browser QA'), 'Pages workflow must run local live runtime browser QA before uploading screenshots')
    assert(workflow.indexOf('Run local live runtime browser QA') < workflow.indexOf(artifact.uploadStep), 'visual QA upload must run after local live runtime QA')
  }

  if (artifact.kind === 'built_bundle_privacy_scan') {
    for (const verifier of ['scan:reference-privacy', 'check:public-projection-privacy', 'check:github-pages-spa-fallback']) {
      assert(packageJson.scripts[verifier], `package.json must expose ${verifier}`)
      assert(String(packageJson.scripts.test).includes(`npm run ${verifier}`), `root test must include ${verifier}`)
    }
    assert(workflow.includes('Scan built Pages privacy'), 'Pages workflow must scan built Pages privacy')
    assert(workflow.indexOf('Scan built Pages privacy') > workflow.indexOf('Build Creator Studio'), 'built Pages privacy scan must run after build')
    assert(workflow.indexOf('Scan built Pages privacy') < workflow.indexOf('Upload artifact'), 'built Pages privacy scan must run before Pages upload')
  }
}

