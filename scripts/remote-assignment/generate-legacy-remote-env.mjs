#!/usr/bin/env node
import { defaultPaths, readJson, writeText } from './lib/io.mjs'
import { toEnvFile } from './lib/env.mjs'
import { assertNoPrivateTerms } from './lib/validators.mjs'

const paths = defaultPaths()
const contract = await readJson(paths.contract)
const isEdgeOnly = contract.runtime_mode === 'edge-only'
const agent = contract.topology.agent

const env = {
  REMOTE_RUNTIME_MODE: contract.runtime_mode,
  REMOTE_OPERATOR_OWNER: contract.operator.owner,
  REMOTE_OPERATOR_PROVIDER: contract.operator.provider,
  REMOTE_RUNTIME_ENVIRONMENT: contract.environment,
  REMOTE_API_SERVICE_ID: contract.topology.data_api.service_id,
  REMOTE_API_ORIGIN: contract.topology.data_api.origin,
  REMOTE_API_SECRETS_CONFIGURED: String(contract.topology.data_api.secrets_configured),
  REMOTE_FRONTEND_SERVICE_ID: contract.topology.frontend.service_id,
  REMOTE_FRONTEND_ORIGIN: contract.topology.frontend.origin,
  REMOTE_FRONTEND_SECRETS_CONFIGURED: String(contract.topology.frontend.secrets_configured),
  REMOTE_AGENT_REMOTE_REQUIRED: String(agent.remote_required),
  REMOTE_AGENT_LOCATION: agent.location,
  REMOTE_AI_GENERATION_CLOUD_RUNTIME: String(agent.ai_generation_cloud_runtime),
  REMOTE_READER_CAN_TRIGGER_AI: String(agent.reader_can_trigger_ai),
}

if (isEdgeOnly) {
  env.REMOTE_AGENT_SERVICE_ID = ''
  env.REMOTE_AGENT_ORIGIN = ''
  env.REMOTE_AGENT_SECRETS_CONFIGURED = 'false'
  env.REMOTE_AGENT_ABSENCE_REASON = 'edge-only runtime: AI generation occurs on user-owned edge device'
} else {
  env.REMOTE_AGENT_SERVICE_ID = agent.remote_service_id
  env.REMOTE_AGENT_ORIGIN = agent.remote_origin
  env.REMOTE_AGENT_SECRETS_CONFIGURED = String(agent.remote_secrets_configured)
}

const body = [
  '# Generated by Remote Assignment Compiler',
  '# Do not edit manually.',
  '# Source: deploy/runtime-production/runtime-assignment.intent.local.json',
  '',
  toEnvFile(env),
].join('\n')

assertNoPrivateTerms(body, 'generated legacy env')
await writeText(paths.legacyEnvGenerated, body)
await writeText(paths.legacyEnvLocal, body)
console.log(JSON.stringify({
  status: 'generated',
  generatedPath: paths.legacyEnvGenerated,
  localPath: paths.legacyEnvLocal,
  runtimeMode: contract.runtime_mode,
}, null, 2))
